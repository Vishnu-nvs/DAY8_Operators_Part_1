x=10
print(bin(x))
y=0b1010 #10
print(y)
z=x==y
print(z)
print("We are using == OP (x==y) :",x==y)#true
print("We are using != OP (x!=y) :",x!=y)#False
print("We are using > OP (x>y) :",x>y)#False
print("We are using < OP (x<y) :",x<y)#false
print("We are using >= OP (x>=y) :",x>=y) #True
print("We are using <= OP (x<=y) :",x<=y)# True